"""
Пользователь вводит количество элементов будущего списка
После этого по очереди по одной вводит любые цифры
Сохранить цифры в список
Отсортировать список по возрастанию и вывести на экран
Пример работы: Введите количество элементов: 3
Введите 1 элемент: 5
Введите 2 элемент: 2
Введите 3 элемент: 4
Вывод: [2, 4, 5]

"""
number_of_list_elements = int(input("Введите кол-во элементов списка: "))
result = []
for i in range(number_of_list_elements):
    num = int(input("Введите {} элемент: ".format(i+1)))
    result.append(num)
result.sort()
print(result)